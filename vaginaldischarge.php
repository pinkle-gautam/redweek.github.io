<p><b>White : </b>The shade of white can extend to include cream or light yellow. If a person has no other symptoms, white discharge is most likely a sign of healthy lubrication.

White, thick, strong-smelling discharge is usually associated with a yeast infection, which can also cause itching or irritation.</p>

<p><b>Prevention :</b> Normal vaginal discharge does not need to be prevented. However, taking the following precautions can sometimes prevent abnormal discharge:

Avoid douching, which can destroy the good bacteria that help prevent vaginal infections.
Wear cotton underwear, which absorbs moisture and may prevent a yeast infection.
Practice safe sex by using a condom, limiting the number of sexual partners, and getting tested regularly for STIs.
Use unscented soaps, tampons, and pads.</p>



            
             
