                <p class="py-4"><b>WHY DO GIRLS HAVE PERIODS ? </b> 
Take a positive approach to explaining to your daughter why women have menstrual cycles. Tell her that a menstrual cycle is part of the natural cycle that allows a woman to get pregnant. When a pregnancy doesn't occur in a menstrual cycle, hormones signal her body to shed the baby-ready uterine lining, which is what happens during a period.
</p>

              <p><b>WHY IS MY PERIOD LATE ?</b>
Don’t panic. It is common for periods to come a little earlier or later as not all women have their period every 28 days. A typical cycle is between 21-34 days. Activities that can affect the onset of your cycle are diet, excessive exercise, stress, medication, and several other lifestyle factors. If your period is taking longer than 35 days to occur and you are sexually active, you should take a pregnancy test even if you are on birth control.
</p>
            <p><b>WHAT SHOULD I KNOW ABOUT PADS,TAMPONS AND MENSTRUAL CUPS ?</b>
The most common products known as “feminine hygiene products” are pads, tampons, and menstrual cups . All 3 methods help you go about your daily life by collecting the blood released during your period.
</p>
             <p><b>WHAT ARE SIDE-EFFECTS OF GETTING A PERIOD ?</b>
Up to 2 weeks before the start of your period, you might start to notice changes in your body. These symptoms, often referred to as Premenstrual Syndrome (PMS), include:
Abdominal cramps , Lower back pain , Headaches , Acne , Fatigue , Swollen or tender breasts , Bloating ,Insomnia , Mood swings.
