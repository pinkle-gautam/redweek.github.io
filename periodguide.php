Tell your daughter that she is probably losing only a few teaspoons of blood during her period, even though it may seem like more. If she is going through a pad or tampon every hour, though, there may be a problem, and you should talk with her doctor.

</p>
        <p><b>CAN I HAVE SEX WHILE ON MY PERIOD ?</b>

Yes! Having sex on your period is completely safe, and the only real drawback is the mess that might develop as a result.
</p>
        <p><b>CAN I GET PREGNANT WHILE ON MY PERIOD ?</b>
Yes. The highest risk of pregnancy is during ovulation, typically 14 days before your period. Although the risk of getting pregnant is lower on your period, it is still important to use condoms or other methods of birth control to avoid any unintended pregnancies.
</p>


