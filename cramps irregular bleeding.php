             <p><b>What are the symptoms of Period Cramps?</b>Though these symptoms indicate your usual period cramps, don’t delay seeing a doctor if the painful
periods literally prevent you from doing your everyday activities, or the symptoms worsen, or if the pain
has suddenly started after the age of 25. Some of the common symptoms are:
• Excruciating pain in the lower abdomen which moves to your lower back and thighs sometimes
