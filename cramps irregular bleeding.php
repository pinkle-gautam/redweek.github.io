<p><b>What causes period cramps?</b>
So, this is what it basically is! During your menstrual cycle, the uterus contracts to shed its lining. The
chemicals in your body called prostaglandins trigger the uterus contractions, helping it push out its lining
when you’re menstruating. Higher levels of prostaglandins are associated with unbearable stomach pain
during periods. </p>

<p><b>What do period cramps feel like?</b>
Your uterus is a muscle and while contracting and relaxing, the pain may feel sharp, a pain similar to the
tightening or aching of a muscle or a pain that can be related to a stomach ache. We know it hurts!
Having said that, dysmenorrhea can be uncomfortable but not something that should keep you away
from school or work, and if it does, it is best to consult your doctor.</p>
<p><b>When do we get period cramps?</b>
Menstrual cramps tend to begin 2-3 days before your period, are at their peak during the first two days of
